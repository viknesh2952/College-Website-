<h2>Library</h2>
<br>
<p>The Library is located in the Administrative block of the college with two floors. The ground floor contains the lending section and e-journal section and the reference section and journal section are in the Ist floor. It is well equipped with 37322 Books, 630 E-Journals and 129 national journals and 5 News Papers . For effective Teaching learning process the library is maintaining a special section which is incorporated with Educational CD’s and DVD’s in the fields of Engineering, Management and Science and Humanities. The library provides news papers to the students in order to update the day to day news. The college events, circulars, placement news and other important events of the society are displayed on the electronics notice board (LCD Monitor) of the central library entrance. Our library is maintaining separate Digital Library centre which has 30 computers with internet facility and 8MBPS broad band connectivity.</p>
<br>
<pre>Area of ground floor   :  326 m2  

First floor                 :  300 m2

working hours:  8.30 am to 6.00 pm on all working days.

The library activities are fully automated with “LIBSOFT” software. For book issue and    return, Bar Coding System is followed.</pre><hr><hr>
<h2>Cafetaria</h2>
<br>
<p>A world-class cafeteria with ultra-modern ambience, having modern working ranges/equipments, to serve a total strength of 200 students at a time. It also has an exclusive mezzanine floor to host dinners/get-togethers etc.</p><br>
    <p>It is located at an easily accessible area. Variety of refreshments and meals available at the college canteen at affordable rates. Adequate seating facilities are provided to accommodate students and staff even during peak time. It tends to be the most popular place on campus, not only for refreshment but also for student bonding, discussions and even work on assignments and projects.</p>
<hr><hr>
<h2>Communication Skills Lab</h2>
<br><p>The Language Lab is an audio visual installation used as an aid in English language teaching with special focus on communication competence and development of soft skills. It is teacher controlled system connected to a number of student consoles, each having a headset with microphone.</p><br>
<p>The fully air-conditioned lab makes use of state of the art facilities like projector and audio system to train students in public speaking, by laying special emphasis on accent training.</p><br>
<p>The cutting edge training, students receive in the Language Lab polishes their reading, listening and writing skills, and add to their employability. It provides opportunities for students to enhance their skills in communication which is a prerequisite to spark their personality and get placed in reputed firms.</p>
