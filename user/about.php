<h2>MANAGEMENT</h2>
<p class="para">SRI.SI.PA.ADITANAR founder of AEI, was born in Kayamozhi on Sep 27,
1907. In 1934, as a Barrister, he visited Singapore to practice law and earn the
investment for his journalistic career. In 1948 he commenced THE DAILY
THANTHI and revolutionized the field of journalism and created a far- reaching
impact on the society. In political arena too, Sri.Si.Pa.Aditanar adorned the
Tamilnadu Assembly speaker post in 1967 and later in 1969 assumed charge as
a Minister for Co-operation and Agriculture. He established AEI in 1962, to uplift
the rural masses by providing quality higher education in Arts, Science, Physical
Education, Teacher Education, Engineering, Management, Medical & Paramedical
Science, etc. which are usually available in urban cities. He passed away in May
24, 1981.</p>
